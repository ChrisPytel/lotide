//"assertObjectsEqualTest.js - used for testing assertObjectsEqual.js"


const assertObjectsEqual = require('../assertObjectsEqual');

//Test runner code

const shirtObject = { color: "red", size: "medium" };
const anotherShirtObject = { size: "medium", color: "red" };
assertObjectsEqual(shirtObject, anotherShirtObject); // => should PASS assertion

const multiColorShirtObject = { colors: ["red", "blue"], size: "medium" };
const anotherMultiColorShirtObject = { size: "medium", colors: ["red", "blue"] };
assertObjectsEqual(multiColorShirtObject, anotherMultiColorShirtObject); // => should PASS assertion